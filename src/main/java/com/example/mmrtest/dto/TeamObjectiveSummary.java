package com.example.mmrtest.dto;

public class TeamObjectiveSummary {
    private int dragons;
    private int heralds;
    private int barons;
    private int towers;
    private int voidgrubs;

    public TeamObjectiveSummary() {
    }

    public int getDragons() {
        return dragons;
    }

    public void setDragons(int dragons) {
        this.dragons = dragons;
    }

    public int getHeralds() {
        return heralds;
    }

    public void setHeralds(int heralds) {
        this.heralds = heralds;
    }

    public int getBarons() {
        return barons;
    }

    public void setBarons(int barons) {
        this.barons = barons;
    }

    public int getTowers() {
        return towers;
    }

    public void setTowers(int towers) {
        this.towers = towers;
    }

    public int getVoidgrubs() {
        return voidgrubs;
    }

    public void setVoidgrubs(int voidgrubs) {
        this.voidgrubs = voidgrubs;
    }
}